const NotificationService = require("../services/NotificationService");
const service = new NotificationService();

// crear notificaci칩n
exports.create = (req, res) => {
  const { type, message, ticketId } = req.body;

  if (!type || !message || !ticketId) { // Validar los par치metros 
    return res.status(400).json({ error: "Faltan par치metros requeridos" });
  }

  const notification = service.create(type, message, ticketId);
  res.status(201).json(notification); // Retorna la notificaci칩n 
};

exports.list = (req, res) => {
  res.status(200).json(service.list());
};
